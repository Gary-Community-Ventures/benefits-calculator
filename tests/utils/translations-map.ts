export function translateToSpanish(englishText: string): string {
  return englishToSpanishTranslations[englishText] || englishText;
}

export function translateTextsToSpanish(englishTexts: { [key: string]: string[] }): { [key: string]: string[] } {
  const spanishTexts: { [key: string]: string[] } = {};
  
  for (const [url, texts] of Object.entries(englishTexts)) {
    spanishTexts[url] = texts.map(text => translateToSpanish(text));
  }
  
  return spanishTexts;
}

export function verifyTranslation(actualText: string, expectedEnglish: string, expectedSpanish: string): boolean {
  if (actualText === expectedEnglish) {
    console.error(`Found English text "${expectedEnglish}" where Spanish "${expectedSpanish}" was expected`);
    return false;
  }
  if (actualText !== expectedSpanish) {
    console.error(`Found incorrect translation "${actualText}" - expected "${expectedSpanish}"`);
    return false;
  }
  return true;
}
